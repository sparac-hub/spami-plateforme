                                'governorate_id' => $createdGovernorate,
                            ]))->id;

                            if ($zones) {
                                foreach ($zones as $zone => $postal_code) {
                                    Zone::create($this->getLocationWithTranslation($locales, $zone, [
                                        'postal_code' => $postal_code,
                                        'city_id' => $createdCity,
                                        'country_id' => $createdCountry,
                                        'governorate_id' => $createdGovernorate,
                                    ]));
                                }
                            }
                        }
                    }
                }
            }

        }

        $ended_at = new \DateTime(date('Y-m-d H:i:s'));

        echo "\n * LocationsTableSeeder took " .
            $started_at->diff($ended_at)->i . ' minutes ' .
            $started_at->diff($ended_at)->s . ' seconds';
    }

    public function getLocationWithTranslation($locales, $name, $attributes = [])
    {
        foreach ($locales as $locale) {
            $attributes[$locale] = ['name' => $name];
        }

        return $attributes;
    }
